// protoc-plugin to generate constants file with the field names.

package main

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
)

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			generateConstants(gen, f)
		}
		return nil
	})
}

// generateConstants generates a _constants.pb.go file containing protobuf constants with field and edges proto names.
func generateConstants(gen *protogen.Plugin, file *protogen.File) *protogen.GeneratedFile {
	filename := file.GeneratedFilenamePrefix + "_constants.pb.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)
	g.P("// Code generated by protoc-gen-go-const. DO NOT EDIT.")
	g.P()
	g.P("// source: ", file.Desc.Path())
	g.P()
	g.P("package ", file.GoPackageName)
	g.P("const(")
	for _, msg := range file.Messages {
		msgName := msg.Desc.Name()
		g.P(fmt.Sprintf("// Fields and Edges constants for %q", msgName))
		msg.Desc.Name()
		for _, field := range msg.Fields {
			entFieldType := "Field"
			if field.Message != nil { // Messages are edges, either direct or back-references
				entFieldType = "Edge"
			}
			g.P(fmt.Sprintf(`%s%s%s = %q`, msgName, entFieldType, field.GoName, field.Desc.Name()))
		}
		g.P()
	}
	g.P(")")

	return g
}
